	<h2 class="title">Flash Tower Defence (under development)</h2>
	<div class = "sub">{ Flash (Actionscript 3.0), PHP, mySQL }</div>
	<div class="entry">
		<p>Taking tower defense games to the next level, this flash game has several unique features.  The game pits two players against each other; each player builds towers for defense and sends creeps for offense.  Therefore, the creeps you are killing in your maze of towers are the creeps the other player sent.  The game uses a customized A* pathfinding algorithm which allows the creeps to find the fastest way through your maze.  However, every creep must visit two nodes along the way.  This allows you to build a much more complex maze (you can make creeps go through the same spot three times).  The key to winning is to balance offense with defense.  After each game you can save your design (the towers you built and the creeps you sent) onto the design board.  Based on who you beat your design will be ranked and other people can then go on and play versus your design.  Using this system adds the element of human creativity and guarantees the game will never become too easy.</p>
		<p>As for project development, the design board is finished along with most of the game.  Currently I am working on adding different types of towers and creeps.  Still on the list is to work on balance and improve the website on which the game will sit.</p>
